/*
  SOLARIUM - Script de TRIGGERS + PROCEDIMIENTOS ALMACENADOS
  Para los 4 módulos (CH, P, C, J) y la bitácora compartida.

  Base objetivo: DB_TiendaBelleza (ajusta si tu BD tiene otro nombre).
  Motor: Microsoft SQL Server.

  Nota:
  - Los triggers registran INSERT/UPDATE/DELETE en BITACORA_OPERACIONES.
  - ACTOR_ID_USUARIO queda NULL (desde triggers no hay contexto del usuario app).
  - Se crean SPs para CRUD y listados con paginación simple.
*/

