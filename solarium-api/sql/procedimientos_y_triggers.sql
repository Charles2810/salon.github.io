CREATE PROCEDURE dbo.sp_Bitacora_ActividadPorDia
  @Days INT = 14
AS
BEGIN
  SET NOCOUNT ON;
  SELECT
    CONVERT(VARCHAR, CAST(FECHA AS DATE), 23) AS dia,
    COUNT(1) AS total
  FROM BITACORA_OPERACIONES
  WHERE FECHA >= DATEADD(DAY, -@Days, GETDATE())
  GROUP BY CAST(FECHA AS DATE)
  ORDER BY dia;
END
GO

