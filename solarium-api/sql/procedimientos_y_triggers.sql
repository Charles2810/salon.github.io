  DROP TRIGGER dbo.TR_CATEGORIAS_AIUD_BITACORA;
GO
CREATE TRIGGER dbo.TR_CATEGORIAS_AIUD_BITACORA
ON dbo.CATEGORIAS
AFTER INSERT, UPDATE, DELETE
AS
BEGIN
  SET NOCOUNT ON;
  BEGIN TRY
    IF EXISTS (SELECT 1 FROM inserted) AND EXISTS (SELECT 1 FROM deleted)
    BEGIN
      INSERT INTO BITACORA_OPERACIONES (OPERACION, TABLA, ID_REGISTRO, DESCRIPCION, ACTOR_ID_USUARIO, FECHA)
      SELECT 'UPDATE', 'CATEGORIAS', d.ID_CATEGORIA, 'Actualización en CATEGORIAS', NULL, GETDATE()
      FROM deleted d;
      RETURN;
    END

    IF EXISTS (SELECT 1 FROM inserted)
    BEGIN
      INSERT INTO BITACORA_OPERACIONES (OPERACION, TABLA, ID_REGISTRO, DESCRIPCION, ACTOR_ID_USUARIO, FECHA)
      SELECT 'INSERT', 'CATEGORIAS', i.ID_CATEGORIA, 'Inserción en CATEGORIAS', NULL, GETDATE()
      FROM inserted i;
      RETURN;
    END

    IF EXISTS (SELECT 1 FROM deleted)
    BEGIN
      INSERT INTO BITACORA_OPERACIONES (OPERACION, TABLA, ID_REGISTRO, DESCRIPCION, ACTOR_ID_USUARIO, FECHA)
      SELECT 'DELETE', 'CATEGORIAS', d.ID_CATEGORIA, 'Eliminación/Desactivación en CATEGORIAS', NULL, GETDATE()
      FROM deleted d;
      RETURN;
    END
  END TRY
  BEGIN CATCH
    -- No bloquear operación por bitácora
  END CATCH
END
GO

