GO
CREATE PROCEDURE dbo.sp_Reservas_ListarPorFecha
  @FechaInicio DATE,
  @FechaFin DATE,
  @Page INT = 1,
  @PageSize INT = 10
AS
BEGIN
  SET NOCOUNT ON;
  DECLARE @Offset INT = (@Page - 1) * @PageSize;

  SELECT COUNT(1) AS total
  FROM RESERVAS
  WHERE FECHA_RESERVA BETWEEN @FechaInicio AND @FechaFin;

  SELECT
    r.ID_RESERVA AS id_reserva,
    c.NOMBRE + ' ' + c.APELLIDO AS cliente,
    s.NOMBRE AS servicio,
    u.NOMBRE + ' ' + u.APELLIDO AS usuario,
    CONVERT(VARCHAR, r.FECHA_RESERVA, 23) AS fecha_reserva,
    CONVERT(VARCHAR, r.HORA_RESERVA, 8) AS hora_reserva,
    r.ESTADO AS estado,
    r.OBSERVACION AS observacion
  FROM RESERVAS r
  JOIN CLIENTES c  ON r.ID_CLIENTE = c.ID_CLIENTE
  JOIN SERVICIOS s ON r.ID_SERVICIO = s.ID_SERVICIO
  JOIN USUARIOS u  ON r.ID_USUARIO = u.ID_USUARIO
  WHERE r.FECHA_RESERVA BETWEEN @FechaInicio AND @FechaFin
  ORDER BY r.FECHA_RESERVA DESC, r.HORA_RESERVA DESC
  OFFSET @Offset ROWS FETCH NEXT @PageSize ROWS ONLY;
END
GO

