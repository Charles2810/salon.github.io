CREATE PROCEDURE dbo.sp_Servicios_Listar
  @IdCategoria INT = NULL,
  @Page INT = 1,
  @PageSize INT = 10
AS
BEGIN
  SET NOCOUNT ON;
  DECLARE @Offset INT = (@Page - 1) * @PageSize;

  SELECT COUNT(1) AS total
  FROM SERVICIOS
  WHERE (@IdCategoria IS NULL OR ID_CATEGORIA = @IdCategoria);

  SELECT
    s.ID_SERVICIO AS id_servicio,
    s.ID_CATEGORIA AS id_categoria,
    s.NOMBRE AS nombre,
    s.DESCRIPCION AS descripcion,
    s.PRECIO AS precio,
    s.DURACION_MINUTOS AS duracion_minutos,
    c.NOMBRE AS categoria,
    s.ESTADO AS estado
  FROM SERVICIOS s
  JOIN CATEGORIAS c ON s.ID_CATEGORIA = c.ID_CATEGORIA
  WHERE (@IdCategoria IS NULL OR s.ID_CATEGORIA = @IdCategoria)
  ORDER BY s.NOMBRE
  OFFSET @Offset ROWS FETCH NEXT @PageSize ROWS ONLY;
END
GO

